const iconClass =
  "flex h-8 w-8 items-center justify-center rounded-md text-muted-foreground transition-colors hover:bg-accent hover:text-foreground";

export function SocialLinks({ className }: { className?: string }) {
  const wa = SOCIAL.whatsapp
    ? `https://wa.me/${SOCIAL.whatsapp}${
        WHATSAPP_MESSAGE ? `?text=${encodeURIComponent(WHATSAPP_MESSAGE)}` : ""
      }`
    : null;

  const links = [
    SOCIAL.instagram && { href: SOCIAL.instagram, label: "Instagram", Icon: InstagramIcon },
    SOCIAL.facebook && { href: SOCIAL.facebook, label: "Facebook", Icon: FacebookIcon },
    wa && { href: wa, label: "WhatsApp", Icon: WhatsappIcon },
  ].filter(Boolean) as { href: string; label: string; Icon: typeof FacebookIcon }[];

  if (links.length === 0) return null;

  return (
    <div className={cn("flex items-center gap-1", className)}>
      {links.map(({ href, label, Icon }) => (
        <a
          key={label}
          href={href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={label}
          title={label}
          className={iconClass}
        >
          <Icon className="h-[18px] w-[18px]" />
        </a>
      ))}
    </div>
  );
}
